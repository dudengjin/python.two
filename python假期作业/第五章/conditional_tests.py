
print("检查两个字符串相等和不等")
print('test' == 'test')
print('test' == 'test'.title())

print("使用函数lower()的测试")
print('test' == 'TEST'.lower())
print('test'.lower() == 'TEST')

print("检查两个数字相等.不等.大于.小于.大于等于.小于等于")
print(5 == 5)
print(5 != 5)
print(5 > 5)
print(5 < 5)
print(5 >= 5)
print(5 <= 5)

print("使用关键and和or的测试")
print('test' == 'test'and'tset' == 'Test')
print('test' == 'test'or'tset' == 'Test')

